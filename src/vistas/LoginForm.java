    public void validar(){
        String dni = txtPass.getText();
        String user= txtUser.getText();
        if(txtUser.getText().equals("")||txtPass.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Debe ingresar los datos para iniciar");
            txtUser.requestDefaultFocus();
        }
        else{
            eu=udao.ValidarUsuario(dni, user);
           // JOptionPane.showMessageDialog(null,"dni: "+eu.getDni()+" Nom "+eu, "Info guardada", JOptionPane.INFORMATION_MESSAGE);
            if(eu.getNom()!= null && eu.getDni() != null){
                Principal p = new Principal();
                p.setVisible(true);
                dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "Debe ingresar usuarios válidos");
                txtUser.requestFocus();
            }
        }
    }
    
    
